from math import *

#Absolute value
num = -1
print(abs(num)) #Return the absolute value of the number

#Power
num2 = 10
print(pow(num2, 2))

num3 = 7

#Max n Min
print(max(num, num2, num3, 150))
print(min(num, num2, num3, 150))

#Round -> closer integer

num4 = 4.2
print(round(num4))

#Floor -> closer integer down
num5 = 4.9
print(floor(num5))

#Cell -> closer integer up
num6 = 4.1
print(ceil(num6))

#Square root
print(sqrt(num6))
