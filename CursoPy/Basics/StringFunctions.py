#Lenght
text = "we're programming in Python"
print(len(text))

#Capitalize
print(text.capitalize())

#Uppercase
print(text.upper())

#Lowercase
print(text.lower())

#Replace
print(text.replace("Python", "Java"))

#Index
print(text.index("Python"))

#Boolean fns
print(text.islower()) #Is the text in lowercase?
print(text.isupper()) #Is the text in uppercase?
print(text.isalnum()) #The text have a num?

